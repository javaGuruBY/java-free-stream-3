public class MathOperationsDemo {

    public static void main(String[] args) {
        double firstNum  = 4;
        double secondNum  = 3;
        System.out.println("a + b = " + (firstNum + secondNum));
        System.out.println("a - b = " + (firstNum - secondNum));
        System.out.println("a * b = " + (firstNum * secondNum));
        System.out.println("a / b = " + (firstNum / secondNum));
    }
}

package by.art.hw2Task3;

public class MathOperationsDemo {

    public static void main(String[] args) {
        double firstNum  = 27;
        double secondNum  = 9;
        System.out.println("Сложение. a + b = " + (firstNum + secondNum));
        System.out.println("Вычитание. a - b = " + (firstNum - secondNum));
        System.out.println("Умножение. a * b = " + (firstNum * secondNum));
        System.out.println("Деление. a / b = " + (firstNum / secondNum));
    }
}
